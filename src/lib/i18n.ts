export type Dict = typeof uz;
const dictionaries: Record<Locale, Dict> = { uz, ru };

// Joriy til lug'ati: const t = useT(); t.home.heroTitle
export function useT(): Dict {
  const locale = useLocaleStore((st) => st.locale);
  return dictionaries[locale];
}

export function useLocale(): Locale {
  return useLocaleStore((st) => st.locale);
}

// Katalog I18nName (uz/ru) ni joriy tilga lokalizatsiya qiladi
export function useLocalize() {
  const locale = useLocaleStore((st) => st.locale);
  return (name?: I18nName | string): string => {
    if (!name) return '';
    if (typeof name === 'string') return name;
    if (locale === 'ru') return name.ru || name.uz || '';
    return name.uz || name.ru || '';
  };
}

// Narx: 350 000 so'm / сум
export function formatPriceT(amount: number, currency: string | undefined, t: Dict): string {
  const cur = !currency || currency === 'UZS' ? t.common.soum : currency;
  return new Intl.NumberFormat('ru-RU').format(amount) + ' ' + cur;
}

// To'liq vaqt: "15 daq oldin" / "15 мин назад"
export function timeAgoT(iso: string, t: Dict): string {
  const min = Math.floor((Date.now() - new Date(iso).getTime()) / 60000);
  if (min < 1) return t.time.justNow;
  if (min < 60) return `${min} ${t.time.min} ${t.time.ago}`;
  const hr = Math.floor(min / 60);
  if (hr < 24) return `${hr} ${t.time.hour} ${t.time.ago}`;
  const day = Math.floor(hr / 24);
  if (day < 30) return `${day} ${t.time.day} ${t.time.ago}`;
  return new Date(iso).toLocaleDateString('ru-RU', { day: 'numeric', month: 'short', year: 'numeric' });
}

// Kartalar uchun qisqa vaqt: "15 daq" / "15 мин"
export function timeAgoShortT(iso: string, t: Dict): string {
  const min = Math.floor((Date.now() - new Date(iso).getTime()) / 60000);
  if (min < 1) return t.time.now;
  if (min < 60) return `${min} ${t.time.min}`;
  const hr = Math.floor(min / 60);
  if (hr < 24) return `${hr} ${t.time.hour}`;
  const day = Math.floor(hr / 24);
  if (day < 30) return `${day} ${t.time.day}`;
  return new Date(iso).toLocaleDateString('ru-RU', { day: 'numeric', month: 'short' });
}
